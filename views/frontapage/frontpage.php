PMDK STMIK Banjarbaru Tahun Akademik 2018/2019
<p>Selasa, 26 Juni 2018 06:18</p>

<p>Banjarbaru, STMIK Banjarbaru sebagai salah satu sekolah tinggi terbesar di Indonesia kembali membuka penerimaan mahasiswa baru jalur penelusuran minat dan kemampuan (PMDK) untuk tahun 2018/2019.</p>
<p>Untuk mendaftar PMDK TA 2018/2019 ini, STMIK Banjarbaru membuka kesempatan seluas-luasnya dan terbuka untuk semua sekolah. Sekolah dan siswanya yang berminat tidak perlu menunggu undangan dari STMIK Banjarbaru (Perhatikan kriteria sekolah dibawah!)- (update!).
PDMK STMIK Banjarbaru terdiri dari dua jenis, yaitu PMDK Akademik dan PMDK Bidikmisi(khusus bagi peserta yang kurang mampu). Peserta hanya boleh mengikuti salah satu jenis PMDK tsb.</p>

</p><br>Persyaratan Umum : </p>
<p> Calon Peserta  PMDK Akademik (AK) STMIK Banjarbaru harus memperlihatkan hal berikut ini: </p>
<p> - Sekolah RSBI (Rintisan Sekolah Bertaraf Internasional) 100% terbaik.</p>
<p> - Sekolah dengan Akreditasi A 50% terbaik </p>
<p> - Sekolah dengan Akreditasi B 25% terbaik </p>
<p> - Sekolah dengan Akreditasi C 15% terbaik </p>
<p> - Sekolah lainnya 5% terbaik </p>
